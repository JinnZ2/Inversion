    lines.append("")

    for i, inv in enumerate(catalog, 1):
        lines.append("-" * 72)
        lines.append(f"[{i}/{len(catalog)}] {inv.id.upper()}")
        lines.append(f"      Status: {inv.status}")
        lines.append("")
        lines.append(f"  CAPACITY DESTROYED:")
        lines.append(f"    {inv.capacity}")
        lines.append("")
        lines.append(f"  HOW IT WAS DESTROYED:")
        for line in inv.destruction.strip().split("\n"):
            lines.append(f"    {line.strip()}")
        lines.append("")
        lines.append(f"  HOW IT'S NOW WEAPONIZED:")
        lines.append(f"    {inv.weaponization}")
