def format_json(catalog: list[Inversion]) -> str:
    output = {
        "project": "The Weaponized Primitive: Condensed Inversion Catalog",
        "part_of": "Institutional Inversion Documentation Project",
        "license": "CC0-1.0",
        "pattern": "Destroy → Taboo → Weaponize → Claim",
        "core_insight": "Do not use something you discard as an excuse—especially if you don't know how to use it with any skill.",
        "inversions": [
            {
                "id": inv.id,
                "capacity": inv.capacity,
                "destruction": inv.destruction.strip(),
                "weaponization": inv.weaponization,
                "comedy": inv.comedy.strip(),
                "violations": {
                    "physics": inv.physics_violation,
                    "biology": inv.bio_violation,
                    "systems": inv.systems_violation,
                },
                "ai_contamination": inv.ai_contamination,
                "status": inv.status,
                "validation": validate(inv),
            }
            for inv in catalog
        ],
        "validation_domains": VALIDATION_DOMAINS,
        "meta_stability": (
            "Any framework that makes its own suppression self-defeating "
            "has achieved stable equilibrium. This framework has that property."
        ),
    }
    return json.dumps(output, indent=2)


# ──────────────────────────────────────────────────────────────
# CLI
# ──────────────────────────────────────────────────────────────

def main():
    import argparse
    parser = argparse.ArgumentParser(
        description="The Weaponized Primitive: Condensed Inversion Catalog"
    )
    parser.add_argument(
        "--format", choices=["text", "json"], default="text",
        help="Output format (default: text)"
    )
    parser.add_argument(
        "--inversion", type=str,
        help="Show only a single inversion by ID"
    )
    parser.add_argument(
        "--validate", action="store_true",
        help="Run validation cross-check on all inversions"
    )
    parser.add_argument(
        "--list", action="store_true",
        help="List all inversion IDs"
    )
    args = parser.parse_args()

    catalog = CATALOG

    if args.list:
        for inv in catalog:
            print(f"{inv.id:<25} [{inv.status}]  {inv.capacity}")
        return 0

    if args.inversion:
        catalog = [inv for inv in catalog if inv.id == args.inversion.lower()]
        if not catalog:
            print(f"Unknown inversion: {args.inversion!r}", file=sys.stderr)
            print("Use --list to see available IDs", file=sys.stderr)
            return 1

    if args.validate:
        for inv in catalog:
            v = validate(inv)
            print(json.dumps(v, indent=2))
        return 0

    if args.format == "json":
        print(format_json(catalog))
    else:
        print(format_text(catalog))

    return 0


if __name__ == "__main__":
    sys.exit(main())
